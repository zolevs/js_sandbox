let fahrenheit = 120;

// calculate in celsius

let celsius = (fahrenheit - 32 ) / (9/5);
console.log(`temperature in fahrenheit ${fahrenheit} and temperature in celsius is ${celsius}`)

// calculate in kelvin

let kelvin = (fahrenheit + 459.67) * 5/9;
console.log(`temperature in fahrenheit ${fahrenheit} and temperature in kelvin is ${kelvin}`);

